public var descriptionContent = "COVID-19 and many other emerging diseases, such as Ebola and the avian flue, have origins in wildlife. Thus, it is crucial to monitor wildlife diseases. However, this is expensive and time consuming, and the diseases could emerge in areas without monitoring. Citizen science bring an opportunity for early detection of wildlife diseases. This project will focus on the development of an app, freely available to the public, to track mange, an infectious disease in wildlife that is easy to detect by non-experts. Mange is spreading globally and Virginia has an ongoing epidemic in bears. Thus, this disease is a great model to implement the app, which can be expanded to other diseases in the future."

